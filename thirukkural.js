// Function to load a random Tirukkural couplet
function loadRandomKural() {
    const container = document.getElementById("tirukkural-container");
    const randomIndex = Math.floor(Math.random() * tirukkuralCouplets.length);
    const kural = tirukkuralCouplets[randomIndex];
    const words = kural.text.split("$");
    words.splice(1, 0, "<br/>");
    const formattedKural = words.join(" ");
    const headingKural =kural.heading

  // Calculate ideal font size
  const containerWidth = container.clientWidth;
  const containerHeight = container.clientHeight;
  const fontSize = Math.min(containerWidth / (formattedKural.length / 2.5), containerHeight);
  const headFontSize=fontSize + 5;
  // Apply font size
  container.innerHTML = "<h2 style='font-size:" + headFontSize + "px'>"+ headingKural +"</h2><p style='font-size:" + fontSize + "px'>" + formattedKural + "</p><p style='text-align:right;color:red;font-size:" + fontSize + "px'>திருவள்ளுவர்</p>";
  
}

// Load a random Tirukkural couplet on page load
window.onload = function() {
    loadRandomKural();
};
   // Re-load a random Tirukkural couplet on window resize
   window.onresize = function() {
    loadRandomKural();
};

setInterval(loadRandomKural, 10000);